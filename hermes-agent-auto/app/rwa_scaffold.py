@router.post('/scaffold/generate', dependencies=[Depends(require_key)])
def generate_scaffold(req: ScaffoldRequest) -> Dict[str, Any]:
    ensure_tables()
    try:
        project = safe_name(req.project_name)
    except ValueError as exc:
        raise HTTPException(status_code=400, detail=str(exc)) from exc
    root = scaffold_root() / project
    if root.exists():
        if not req.overwrite:
            raise HTTPException(status_code=400, detail='project already exists; set overwrite=true')
        shutil.rmtree(root)
    root.mkdir(parents=True, exist_ok=True)
    files = generate_files(root, req)
    manifest = {'project_name': project, 'root_path': str(root), 'file_count': len(files), 'files': files, 'created_at': now()}
    with db.connect() as conn:
        conn.execute('INSERT INTO rwa_mine_scaffolds (project_name, root_path, file_count, manifest_json, created_at) VALUES (?, ?, ?, ?, ?)', (project, str(root), len(files), json.dumps(manifest, ensure_ascii=False), now()))
    db.audit('rwa_mine_scaffold_generate', 'rwa_mine_scaffold', project, {'file_count': len(files), 'root_path': str(root)}, 'success', 'medium', 'not_required')
    return {'status': 'success', 'manifest': manifest}


@router.get('/scaffold/list')
def list_scaffolds() -> List[Dict[str, Any]]:
    ensure_tables()
    with db.connect() as conn:
        return [dict(r) for r in conn.execute('SELECT id, project_name, root_path, file_count, created_at FROM rwa_mine_scaffolds ORDER BY id DESC LIMIT 100').fetchall()]


@router.get('/scaffold/latest')
def latest_scaffold() -> Dict[str, Any]:
    ensure_tables()
    with db.connect() as conn:
        r = conn.execute('SELECT * FROM rwa_mine_scaffolds ORDER BY id DESC LIMIT 1').fetchone()
    if not r:
        raise HTTPException(status_code=404, detail='no scaffold found')
    data = dict(r)
    data['manifest'] = json.loads(data.pop('manifest_json'))
    return data
