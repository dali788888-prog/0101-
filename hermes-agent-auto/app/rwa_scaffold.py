def require_key(x_hermes_api_key: str = Header(default='')) -> None:
    settings = get_settings()
    if settings.hermes_agent_api_key and x_hermes_api_key != settings.hermes_agent_api_key:
        raise HTTPException(status_code=401, detail='Invalid API key')


def now() -> str:
    return datetime.now(timezone.utc).isoformat()


def safe_name(value: str) -> str:
    name = re.sub(r'[^a-zA-Z0-9_-]+', '-', value.strip()).strip('-').lower()
    if not name:
        raise ValueError('project_name is empty')
    return name[:80]


class ScaffoldRequest(BaseModel):
    project_name: str = Field(default='rwa-mine-mvp', min_length=2)
    overwrite: bool = False
    include_frontend: bool = True
    include_contracts: bool = True
    include_backend: bool = True
    include_infra: bool = True
    include_docs: bool = True


def scaffold_root() -> Path:
    return Path('/app/storage/rwa_mine_projects')


def write(path: Path, content: str) -> Dict[str, Any]:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.strip() + '\n', encoding='utf-8')
    return {'path': str(path), 'size': path.stat().st_size}


